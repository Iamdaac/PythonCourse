# Primitive Data Types

# Subscripting 
print("hello"[4])
# This method of getting a specific element is called subscripting.
# We can use negative numbers like "-1" this would call the last element.

# String
print("123" + "456")

# Integer = Whole Number
print(12 + 456)

# Large numbers
print(322_456_987)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)